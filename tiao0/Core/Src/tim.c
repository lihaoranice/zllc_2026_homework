/**
  * @brief  电机调零函数 - 设置PWM为中位值(1.5ms)
  * @retval None
  */
void Motor_Zero(void)
{
  // 设置PWM占空比为0度位置(1000us，对应Period=19999时的CCR值)
  // 1.0ms / 20ms * 20000 = 1000
  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 1000);
}

/**
  * @brief  设置舵机角度
  * @param  angle: 角度值 (0到180度)
  * @retval None
  */
void Servo_SetAngle(uint16_t angle)
{
  // 限制角度范围在0到180度之间
  if(angle > 180) angle = 180;
  
  // 舵机角度转换公式: 脉冲宽度 = 1000 + angle * (1000 / 180)
  // 即: 脉冲宽度 = 1000 + angle * 5.556
  uint32_t pulse = 1000 + (uint32_t)(angle * 5.556);
  
  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, pulse);
}

/**
  * @brief  设置电机PWM值
  * @param  pulse: PWM脉宽值 (0-20000对应0-100%占空比)
  * @retval None
  */
void Motor_SetPWM(uint32_t pulse)
{
  if(pulse > 20000)
  {
    pulse = 20000;
  }
  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, pulse);
}

